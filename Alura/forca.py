import random

def joga():

    imprime_mensagem_abertura()
    
    escolha = int(input("Escolha a dificuldade: \n(1) Facil \n(2) Medio \n(3) Dificil \nR: "))

    if escolha == 1:
        escolha_facil = int(input("Digite qual tema você quer jogar: \n(1) Animal \n(2) Fruta \nR: "))
        if escolha_facil == 1:
            palavra_secreta = carrega_palavra_secreta_animais()
            print("Dica: Animal")

        else:
            palavra_secreta = carrega_palavra_secreta()
            print("Dica: Fruta")
    
        
    elif escolha == 2:
        palavra_secreta = carrega_palavra_secreta_objetos()
        print("Dica: Objetos")
    
    else:
        palavra_secreta = carrega_palavra_secreta_tudo()
        print("Dica: Tudo")

    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while(not enforcou and not acertou):

        chute = pede_chute()

        if(chute in palavra_secreta):
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas

        print(letras_acertadas)

    if(acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)


